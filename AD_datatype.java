
public class AD_datatype {
    public static void main(String args[]) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';          //for single character only
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float price = 12;
        System.out.print(price);  //output = 12.00000
        //double = for a larger value after .
        //long = for longer numbers
        //short = for numbers
    }
}
